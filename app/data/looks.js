export function getLookByNumber(lookNumber) {
  return looks.find((look) => look.number === lookNumber)
}

export function getWrappedLookNumber(lookNumber) {
  const totalLooks = looks.length

  if (lookNumber < 1) {
    return totalLooks
  }

  if (lookNumber > totalLooks) {
    return 1
  }

  return lookNumber
}